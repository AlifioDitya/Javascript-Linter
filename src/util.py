def loading(message):
    print(f"{message}", end="\r")
    sleep(0.1)
    print(f"{message}.",end="\r")
    sleep(0.1)
    print(f"{message}..",end="\r")
    sleep(0.1)
    print(f"{message}...",end="\r")

